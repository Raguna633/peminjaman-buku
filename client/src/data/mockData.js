// Helper function untuk generate ID
export const generateId = (prefix) => {
  const timestamp = Date.now().toString(36);
  const random = Math.random().toString(36).substr(2, 5);
  return `${prefix}${timestamp}${random}`.toUpperCase();
};

// Helper function untuk format tanggal
export const formatDate = (dateString) => {
  if (!dateString) return "-";
  const options = { day: "numeric", month: "long", year: "numeric" };
  return new Date(dateString).toLocaleDateString("id-ID", options);
};

// Helper function untuk menghitung denda
export const calculateFine = (dueDate, returnDate) => {
  const due = new Date(dueDate);
  const returned = new Date(returnDate);
  const diffTime = returned - due;
  const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
  
  if (diffDays > 0) {
    return diffDays * 1000; // Rp 1.000 per hari keterlambatan
  }
  return 0;
};

// Statistik untuk dashboard
export const getStatistics = () => {
  const totalBooks = books.reduce((acc, book) => acc + book.stok, 0);
  const borrowedBooks = books.reduce((acc, book) => acc + book.dipinjam, 0);
  const totalMembers = users.filter((user) => user.role === "user").length;
  const activeTransactions = transactions.filter((t) => t.status === "dipinjam").length;
  const totalTransactions = transactions.length;
  
  return {
    totalBooks,
    borrowedBooks,
    availableBooks: totalBooks - borrowedBooks,
    totalMembers,
    activeTransactions,
    totalTransactions,
  };
};
